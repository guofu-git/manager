package DuoChongXunHuan;
//输出矩形*
public class PrintRectangle {
    public static void main(String[] args) {
        //输出一行星星
        for (int i=1;i<=5;i++){
            //输出一列星星
            for (int j=1; j<=5; j++){
                System.out.print("*");
            }
            System.out.println();
        }


    }
}
